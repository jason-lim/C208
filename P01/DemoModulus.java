public class DemoModulus {

	public static void main(String[] args) {
		// The main method begins the execution of this application.
		
		System.out.println("The difference between divide / and modulus %.");
		//Calculation
		System.out.println(377 / 8); //divide
		System.out.println(377 % 8); //modulus
		
		//Data-types: int vs double
		System.out.println(4 / 2.0);
		System.out.println(4 / 2);
		System.out.println(3 * 1.5);
		
	}// end of main method
}// end of class