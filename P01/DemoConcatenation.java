
public class DemoConcatenation {

	public static void main(String[] args) {

		String moduleName = "C208"; 
		String schoolName = "SOI";
		String finalResult = moduleName + " at " + schoolName;
		System.out.println(finalResult); 
		// C208 at SOI

		int output = 10;
		System.out.println("The value is " + output);
		// The value is 10

		int num1 = 4; 
		int num2 = 6;
		System.out.println("The sum is " + num1 + num2);
		// The sum is 46

		System.out.println("The sum is " + (num1 + num2));
		// The sum is 10
	}
}


